"""Схемы коллекций «ключ + отображаемое имя» раздела ЭЦП IPS.

References:
    ``GET /api/signs/*`` — массивы DTO ``*KeyAndNameCollectionItemOutContract``.
"""

from pydantic import Field

from ..base import IPSModel


class StringKeyName(IPSModel):
    """Элемент справочника ЭЦП со строковым ключом и отображаемым именем.

    Унифицированная пара «ключ + имя» для коллекций раздела электронной подписи,
    где ключ — строка. Применяется для графов подписания (схем согласования/
    подписания документа): ключ идентифицирует граф, а ``display_name`` — его
    человекочитаемое название для отображения в UI выбора графа.

    Оба поля необязательны (сервер может опускать любое из них); отсутствующее
    значение приходит как ``None``.

    Attributes:
        id: Строковый ключ элемента (идентификатор графа подписания). ``None``,
            если сервер не вернул ключ.
        display_name: Отображаемое имя элемента для UI. ``None``, если не задано.

    Notes:
        DTO ``StringKeyAndNameCollectionItemOutContract``. JSON-поля —
        ``id`` и ``displayName`` (camelCase сопоставляется автоматически).
    """

    id: str | None = Field(default=None, description="Строковый ключ элемента")
    display_name: str | None = Field(default=None, description="Отображаемое имя элемента")


class IntKeyName(IPSModel):
    """Элемент справочника ЭЦП с числовым ключом и отображаемым именем.

    Унифицированная пара «ключ + имя» для коллекций раздела электронной подписи,
    где ключ — целое число. Применяется для рангов подписи (уровней/категорий
    подписанта) и для дополнительных параметров вывода ЭЦП (параметров печати/
    отображения штампа подписи на документе и в карточке пользователя): ключ
    идентифицирует элемент, ``display_name`` — его название для UI.

    Ключ объединяет int32- и int64-варианты DTO (оба укладываются в Python ``int``).
    По умолчанию ключ — ``0``; имя необязательно и приходит ``None``, если не задано.

    Attributes:
        id: Числовой ключ элемента (ранг подписи либо код параметра вывода).
            По умолчанию ``0``.
        display_name: Отображаемое имя элемента для UI. ``None``, если не задано.

    Notes:
        DTO ``Int64KeyAndNameCollectionItemOutContract`` /
        ``Int32KeyAndNameCollectionItemOutContract``. JSON-поля — ``id`` и
        ``displayName`` (camelCase сопоставляется автоматически).
    """

    id: int = Field(default=0, description="Числовой ключ элемента")
    display_name: str | None = Field(default=None, description="Отображаемое имя элемента")
