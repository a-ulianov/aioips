"""Схема входных данных для шифрования лицензии IPS.

References:
    ``POST /core/api/licenses/encrypt`` — ``EncryptLicenseDto``.
"""

from pydantic import Field

from ..base import IPSModel


class EncryptLicense(IPSModel):
    """Данные для серверного шифрования лицензионной информации (тело запроса).

    Описывает входные параметры операции шифрования лицензии: к какому приложению
    относится лицензия (``app_id``), к какому клиенту она привязана (``client_id``) и
    собственно полезную нагрузку для шифрования (``data_as_base64_string``).
    Используется методом :meth:`licenses_encrypt` как тело POST-запроса.

    Несмотря на POST-глагол, операция является чтением/преобразованием без побочных
    эффектов на сервере: это чистая функция шифрования (сервер ничего не сохраняет),
    нужная для клиентской подготовки лицензионных данных перед их предъявлением.

    Attributes:
        app_id: Идентификатор приложения (``appId``), для которого выпускается лицензия.
            Целое 32-битное число. Обязателен на стороне API (дефолт ``0`` — заглушка,
            задайте явно перед вызовом).
        client_id: Идентификатор клиента (``clientId``) — строка, идентифицирующая
            клиента/окружение, к которому привязывается лицензия (например, значение,
            полученное методом :meth:`generate_client_id`). Не должна быть пустой.
        data_as_base64_string: Полезная нагрузка для шифрования в виде строки Base64
            (``dataAsBase64String``). Не должна быть пустой.
    """

    app_id: int = Field(default=0, description="Идентификатор приложения (appId)")
    client_id: str = Field(default="", description="Идентификатор клиента (clientId)")
    data_as_base64_string: str = Field(
        default="", description="Данные для шифрования в виде строки Base64"
    )
