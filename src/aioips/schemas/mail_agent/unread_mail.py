"""Схема сведений о непрочитанной почте пользователя IPS.

References:
    ``GET /core/api/MailAgent/mailbox/unreadMail`` — ``UnreadMailDTO``.
"""

from datetime import datetime

from pydantic import Field

from ..base import IPSModel


class UnreadMail(IPSModel):
    """Сводка по непрочитанным письмам почтового ящика пользователя IPS.

    Возвращается почтовым агентом IPS и описывает количество непрочитанных писем,
    сгруппированных по важности (высокая / обычная / низкая), а также момент времени
    последней проверки почтового ящика. Все четыре поля обязательны в ответе сервера.

    Используется для индикации новой почты (например, бейджа с числом непрочитанных)
    и для понимания, насколько свежи эти данные (по ``last_check_time``).

    Attributes:
        high_count: Количество непрочитанных писем с высокой важностью.
        normal_count: Количество непрочитанных писем с обычной важностью.
        low_count: Количество непрочитанных писем с низкой важностью.
        last_check_time: Время последней проверки почтового ящика (UTC).
    """

    high_count: int = Field(description="Количество непрочитанных писем высокой важности")
    normal_count: int = Field(description="Количество непрочитанных писем обычной важности")
    low_count: int = Field(description="Количество непрочитанных писем низкой важности")
    last_check_time: datetime = Field(description="Время последней проверки почтового ящика")
